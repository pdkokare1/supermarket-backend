/* controllers/distributorController.js */
'use strict';

const Distributor = require('../models/Distributor');
const Expense = require('../models/Expense');
const Order = require('../models/Order'); // Used for B2B Purchase Orders tracking
const AppError = require('../utils/AppError');
const { withTransaction } = require('../utils/dbUtils');

exports.getAllDistributors = async (request, reply) => {
    const distributors = await Distributor.find({ isActive: true }).lean();
    return { success: true, data: distributors };
};

exports.createDistributor = async (request, reply) => {
    const { name, contactPerson, phone, email, serviceablePincodes } = request.body;
    
    const existing = await Distributor.findOne({ name });
    if (existing) {
        throw new AppError('Distributor with this name already exists', 400);
    }

    const newDistributor = new Distributor({
        name,
        contactPerson,
        phone,
        email,
        serviceablePincodes: serviceablePincodes || []
    });

    await newDistributor.save();
    return { success: true, message: 'Distributor created successfully', data: newDistributor };
};

exports.logPayment = async (request, reply) => {
    const { amount, paymentMode, referenceNote } = request.body;
    const distributorId = request.params.id;

    if (!amount || amount <= 0) {
        throw new AppError('Valid payment amount is required', 400);
    }

    const updatedDistributor = await withTransaction(async (session) => {
        const distributor = await Distributor.findById(distributorId).session(session);
        if (!distributor) throw new AppError('Distributor not found', 404);

        if (distributor.totalPendingAmount < amount) {
            throw new AppError(`Cannot overpay. Maximum pending balance is Rs ${distributor.totalPendingAmount}`, 400);
        }

        distributor.totalPendingAmount -= amount;
        distributor.totalPaidAmount += amount;
        
        distributor.paymentHistory.push({
            amountPaid: amount,
            paymentDate: new Date(),
            paymentMode: paymentMode || 'Bank Transfer',
            referenceNote: referenceNote || 'Cleared via Admin Portal'
        });

        await distributor.save({ session });

        // Log this in the global expense ledger for HQ reconciliation
        const expense = new Expense({
            description: `Supplier Payment: ${distributor.name}`,
            amount: amount,
            category: 'Cost of Goods Sold (COGS)',
            paymentMethod: paymentMode || 'Bank Transfer',
            loggedBy: request.user ? request.user.username : 'System',
            storeId: request.user && request.user.tenantId ? request.user.tenantId : null
        });
        await expense.save({ session });

        return distributor;
    });

    return { success: true, message: 'Payment logged to ledger', data: updatedDistributor };
};

