exports.getDistributorPOs = async (request, reply) => {
    // Authenticates the distributor and fetches only B2B orders assigned to them
    const distributorId = request.user && request.user.distributorId ? request.user.distributorId : request.params.id;
    
    if (!distributorId) {
        throw new AppError('Distributor ID context missing', 403);
    }

    // Pulling drafted and active POs generated by local shops
    const purchaseOrders = await Order.find({
        "items.distributorId": distributorId,
        orderType: 'B2B_RESTOCK'
    }).sort({ createdAt: -1 }).lean();

    return { 
        success: true, 
        message: 'B2B Purchase Orders retrieved', 
        count: purchaseOrders.length,
        data: purchaseOrders 
    };
};

exports.updatePurchaseOrderStatus = async (request, reply) => {
    const { status, trackingDetails } = request.body;
    const orderId = request.params.orderId;
    const distributorId = request.user && request.user.distributorId ? request.user.distributorId : request.body.distributorId;

    const validStatuses = ['Accepted', 'Processing', 'Dispatched', 'Delivered'];
    if (!validStatuses.includes(status)) {
        throw new AppError('Invalid B2B fulfillment status', 400);
    }

    const order = await Order.findOneAndUpdate(
        { _id: orderId, "items.distributorId": distributorId },
        { 
            $set: { 
                fulfillmentStatus: status,
                trackingLink: trackingDetails || undefined
            } 
        },
        { new: true }
    );

    if (!order) {
        throw new AppError('Purchase Order not found or unauthorized access', 404);
    }

    return { 
        success: true, 
        message: `B2B Order marked as ${status}`, 
        data: order 
    };
};
